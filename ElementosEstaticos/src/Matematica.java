
public class Matematica {
	// criação de um  metodo estatico 
	// Static e util para nao precisar instanciar um objeto para acessar o metodo
	public static int somar(int a, int b) {
		return a+b;
	}
	
	// acima retornar uma soma e esse retoma uma subtracao
	public static int subtrair(int a, int b) {
		return a-b;
	}
}
