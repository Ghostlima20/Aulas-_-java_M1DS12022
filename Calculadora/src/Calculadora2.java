
public class Calculadora2 {

	public static void main(String[] args) {
		// expondo as variaveis
		double n1=10;
		double n2=3;
		double resultSoma;
		double resultSub;
		double resultMult;
		double resultDiv;
		
		// mostra as contas e variaveis criadas
		resultSoma = n1+n2;
		System.out.println("A soma é: " + resultSoma);
		
		resultSub = n1-n2;
		System.out.println("A subtração é: " + resultSub);
		
		resultMult = n1*n2;
		System.out.println("A multiplicação é: " + resultMult);
		
		resultDiv = n1/n2;
		System.out.println("A divisão é: " + resultDiv);
		
	}

}
